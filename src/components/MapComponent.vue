<template>
  <l-map
    style="height: 750px"
    :zoom="zoom"
    :center="center"
    @click="getLocation"
  >
    <l-tile-layer :url="url" :attribution="attribution"></l-tile-layer>

    <l-circle
      v-if="isCurrent"
      :lat-lng="this.selectedLocation"
      :radius="circle.radius"
      :color="circle.color"
    />
  </l-map>
</template>

<script>
import { mapState, mapActions } from "vuex";
import { latLng } from "leaflet";
import { LMap, LTileLayer, LMarker, LCircle } from "vue2-leaflet";
import dangerIcon from "../assets/danger.png";
import currentIcon from "../assets/asterisk.png";

export default {
  name: "main-map",
  components: {
    LMap,
    LTileLayer,
    LMarker,
    LCircle,
  },
  data() {
    return {
      url: "https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png",
      attribution:
        '&copy; <a target="_blank" href="http://osm.org/copyright">OpenStreetMap</a> contributors',
      zoom: 10,
      center: latLng(32.0619, 35.1124),
      eventIcon: L.icon({
        iconUrl: dangerIcon,
        iconSize: [22, 22],
        iconAnchor: [16, 37],
      }),
      currentIcon: L.icon({
        iconUrl: currentIcon,
        iconSize: [22, 22],
        iconAnchor: [16, 37],
      }),
      isCurrent: false,
      circle: {},
    };
  },
  methods: {
    ...mapActions(["changeSelectedLocation"]),
    getLocation(event) {
      this.circle = {
        center: [event.latlng.lat, event.latlng.lng],
        radius: 400,
        color: "red",
      };
      this.isCurrent = true;
      this.changeSelectedLocation([event.latlng.lat, event.latlng.lng]);
    },
  },
  computed: {
    ...mapState(["selectedLocation"]),
  },
};
</script>
